var a = 10;
var b = 20;
document.write(a == b); // return false
document.write(a === b); // return false
document.write(a != b); // return true
document.write(a !== b); // return true
document.write(a > b); // return false
document.write(a >= b); // return false
document.write(a < b); // return true
document.write(a <= b); // return true
