let a = 10;  let b = 5;
console.log(a);   //  return 10
let c = a += b;
console.log(c);   //return  15
let d = a -= b;
console.log(d);   // return 5
let e = a *= b;
console.log(e);   // return 50
let f = a /= b;
console.log(f);   // return 2
let g = a %= b;
console.log(g);   // return 2
