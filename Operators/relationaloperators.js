var a = 10;
var b = 20;
console.log(a == b); // return false
console.log(a === b); // return false
console.log(a != b); // return true
console.log(a !== b); // return true
console.log(a > b); // return false
console.log(a >= b); // return false
console.log(a < b); // return true
console.log(a <= b); // return true
